          <h1 class="animated wow fadeIn" data-wow-delay="0s" style="margin-left: -4px">
          <div class="case-study-tag">CASE STUDY</div>
          <div class="case-study-head">EmployeeChannel</div></h1><br>

          <div class="case-title">Problem</div>
          <div class="JE-subhead">Employee benefits and professional development questions often leave workers at a loss:</div><p class="JE-description">Who do I ask about a new medical insurance card? When does my next review cycle close? How many vacation days have I accrued? Often, the path to finding the right answers are unclear and confusing, leaving the workers frustrated and misinformed, and the company in the dark about the pressing questions and needs of the employees.</p>

          <div class="case-title">Challenge</div>
              <div class="JE-subhead">Create a 24-7-365 solution which:</div>

              <ol>
                <li class="normal-li-num">Answers questions on demand and provides timely, personal information to employees</li>
                <li class="normal-li-num">Leads employees through basic task flows while engaging and creating “stickiness”</li>
                <li class="normal-li-num">Tracks interactions to confirm employee tasks and questions</li>
                <li class="normal-li-num">Reduces the time HR teams spend answering repetitive and detailed questions</li>
              </ol>

              <div class="case-title">Approach</div>
              <ol>
                <li class="normal-li-num">A competitive analysis and stakeholder interviews with HR experts to understand how they engages with employees and how they spend and allocate their time</li>
                <li class="normal-li-num">A "task mapping" exercise to understand how HR team members can use the app. We grouped and organized the brainstorm tasks to help inform how HR would build “push” communications</li>
                <li class="normal-li-num">Multiple prototypes used to test internal stakeholders to learn the technical requirements of the MVP, ease of use and obtain buy-in from domain experts</li>
              </ol>

              <div class="case-title">Insights</div>
                <div class="JE-subhead">UX research of the initial MVP found that employee respondents:</div>
                <ul>
                  <li class="normal-li">
                    would engage in a mobile experience if it wasn’t too difficult to set-up
                  </li>

                  <li class="normal-li">
                    would need to know the app originated from their company
                  </li>

                  <li class="normal-li">
                    would need to understand the app was something that the company wanted them to use
                  </li>

                  <li class="normal-li">
                    would not want to feel obligated to use all app features or the app itself
                  </li>

                  <li class="normal-li">
                    who recognized they could find answers to their questions would use the app more for other topics as well
                  </li>
                </ul>

